import numpy as np

a = np.array([
    [2,4],
    [4,5]
])
b = np.array([
    [4,5],
    [6,9]
])
# addition of 2D array
result = a + b
print(result)

# subtraction 
print("subtraction:" ,a - b)

# multiplication
print("multiplication :",a * b)

# division 
print("division :",a / b)

# floor divison
print("floor division:",a // b)

# modulus
print("modulus:",a % b)


